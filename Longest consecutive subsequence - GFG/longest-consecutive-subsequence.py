class Solution:
    
    # arr[] : the input array
    # N : size of the array arr[]
    
    #Function to return length of longest subsequence of consecutive integers.
    def findLongestConseqSubseq(self,arr, N):
        #code here
        p=0
        c=0
        s=set(arr)
        for i in range(max(arr)+1):
            if i in s:
                p+=1
            else:
                c=max(p,c)
                p=0
        c=max(p,c)
        return c

